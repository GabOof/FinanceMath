    private void cadastrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarBtnActionPerformed
        //Cadastra os valores no BD e insere na jTable
        String nome = nomeTxt.getText();
        String classificacao = classificacaoTxt.getText();
        Double valor = Double.valueOf(valorTxt.getText());
        String data = dataTxt.getText();

        if (gastoBtn.isSelected()) {
            valor *= -1; // Faz o valor ficar negativo pras despesas totais serem calculadas
        }

        Carteira carteira = new Carteira();
        carteira.setNome(nome);
        carteira.setClassificacao(classificacao);
        carteira.setValor(valor);
        carteira.setData(data);

        CarteiraDAO clienteDAO = new CarteiraDAO();
        clienteDAO.inserir(carteira);

        nomeTxt.setText("");
        classificacaoTxt.setText("");
        valorTxt.setText("");
        dataTxt.setText("");

        Listar();
        atualizarValoresNaTela();
    }//GEN-LAST:event_cadastrarBtnActionPerformed

