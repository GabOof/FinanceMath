    //Atualiza os valores de ganho, gasto e diferença na interface
    private void atualizarValoresNaTela() {
        CarteiraDAO valores = new CarteiraDAO();

        if (!valores.isVazio()) {
            CarteiraDAO carteiraDAO = new CarteiraDAO();
            double totalGanhos = carteiraDAO.calcularTotalGanhos(); //chamando método que calcula o total de ganhos
            double totalGastos = carteiraDAO.calcularTotalGastos(); //chamando método que calcula o total de gastos
            double diferenca = totalGanhos - totalGastos; //Fazendo o claculo da diferença

            //Atualizando os jLabel com os valores
            recebidoTxt.setText(String.format("%.2f", totalGanhos));
            gastoTxt.setText(String.format("%.2f", totalGastos));
            diferencaTxt.setText(String.format("%.2f", diferenca));
            recebidoTxt.setVisible(true);
            jLabel5.setVisible(true);
            gastoTxt.setVisible(true);
            jLabel7.setVisible(true);
            diferencaTxt.setVisible(true);
            jLabel9.setVisible(true);
        } else {
            // Esconde campos de soma e diferença
            recebidoTxt.setVisible(false);
            jLabel5.setVisible(false);
            gastoTxt.setVisible(false);
            jLabel7.setVisible(false);
            diferencaTxt.setVisible(false);
            jLabel9.setVisible(false);

            // Mostra mensagem
            JOptionPane.showMessageDialog(this, "Não há entradas cadastradas até o momento.");
        }
    }

    //Chama o metodo listarCarteira() apara ser usado na interface e lista os valores
    public void Listar() {
        try {
            CarteiraDAO carteiraDAO = new CarteiraDAO();

            // Adiciona um observador
            CarteiraLogger carteiraLogger = new CarteiraLogger();
            carteiraDAO.addObserver(carteiraLogger);

            List<Carteira> carteira = carteiraDAO.listarCarteira();

            DefaultTableModel model = (DefaultTableModel) entradasTabela.getModel();
            model.setNumRows(0);

            for (Carteira c : carteira) {
                model.addRow(new Object[]{
                    c.getId(),
                    c.getNome(),
                    c.getClassificacao(),
                    c.getValor(),
                    c.getRealDate(),
                    c.getData()
                });
            }
        } catch (Exception e) {
        }
    }

    public FrameApp() {
        this.conexao = new Conexao();
        this.conn = this.conexao.getInstance();
        initComponents();
        carteiraDAO = new CarteiraDAO();
        Listar();
        atualizarValoresNaTela();
    }

