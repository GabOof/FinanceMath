    private void deletarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarBtnActionPerformed
        //Deleta os valores que forem selecionados na tabela tanto na jTable/BD
        try {
            Carteira carteira = new Carteira();
            carteira.setId(Integer.parseInt(jtID.getText()));

            CarteiraDAO dao = new CarteiraDAO();

            int input = JOptionPane.showConfirmDialog(null,
                    "Tem certeza de que deseja excluir?", "Selecione uma opção...", JOptionPane.YES_NO_CANCEL_OPTION);

            if (input == 0) {
                dao.excluir(carteira);
                JOptionPane.showMessageDialog(null, "Dados excluidos com sucesso.");
            } else {
                JOptionPane.showMessageDialog(null, "Operação cancelada.");
            }

            Listar();

            nomeTxt.setText("");
            classificacaoTxt.setText("");
            valorTxt.setText("");
            dataTxt.setText("");

            Listar();
            atualizarValoresNaTela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir os dados.");
        }
    }//GEN-LAST:event_deletarBtnActionPerformed

